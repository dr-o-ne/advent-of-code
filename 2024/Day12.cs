using AdventOfCode.Common;
using Xunit;

namespace AdventOfCode._2024;

using Region = HashSet<(int, int)>;

public sealed class Day12
{
    public object Solve1(string input) =>
        Solve(input, region => GetArea(region) * GetPerimiter(region));

    public object Solve2(string input) =>
        Solve(input, region => GetArea(region) * GetCornersCount(region));

    private static int Solve(string input, Func<Region, int> calculationFunc)
    {
        var grid = new Grid2D<char>(input);

        var result = 0;

        var processedRegion = new Region();
        foreach (var (Y, X, _) in grid)
        {
            if (processedRegion.Contains((Y, X)))
                continue;

            var region = GetRegion(grid, Y, X);
            result += calculationFunc(region);

            processedRegion.UnionWith(region);
        }

        return result;
    }

    private static Region GetRegion(Grid2D<char> grid, int y, int x)
    {
        var result = new Region();

        var value = grid[y, x];
        var queue = new Queue<(int, int)>();
        queue.Enqueue((y, x));

        while (queue.Count > 0)
        {
            (int Y, int X) = queue.Dequeue();
            if (result.Contains((Y, X)))
                continue;

            if (X > 0 && grid[Y, X - 1] == value)
                queue.Enqueue((Y, X - 1));

            if (X < grid.Columns - 1 && grid[Y, X + 1] == value)
                queue.Enqueue((Y, X + 1));

            if (Y > 0 && grid[Y - 1, X] == value)
                queue.Enqueue((Y - 1, X));

            if (Y < grid.Rows - 1 && grid[Y + 1, X] == value)
                queue.Enqueue((Y + 1, X));

            result.Add((Y, X));
        }

        return result;
    }

    private static int GetArea(Region region) =>
        region.Count;

    private static int GetPerimiter(Region region)
    {
        var result = 0;

        foreach((int Y, int X) in region)
        {
            if (!region.Contains((Y, X - 1)))
                result++;
            if (!region.Contains((Y, X + 1)))
                result++;
            if (!region.Contains((Y - 1, X)))
                result++;
            if (!region.Contains((Y + 1, X)))
                result++;
        }

        return result;
    }

    private static int GetCornersCount(Region region)
    {
        var result = 0;
        
        foreach ((int Y, int X) in region)
        {
            // .  
            // x .            
            if (!region.Contains((Y - 1, X)) && !region.Contains((Y, X + 1)))
                result++;
            // . x
            //   .
            if (!region.Contains((Y, X - 1)) && !region.Contains((Y + 1, X)))
                result++;
            //   .
            // . x
            if (!region.Contains((Y, X - 1)) && !region.Contains((Y - 1, X)))
                result++;
            // x .
            // .  
            if (!region.Contains((Y, X + 1)) && !region.Contains((Y + 1, X)))
                result++;


            // x . 
            // x x
            if (region.Contains((Y - 1, X)) && region.Contains((Y, X + 1)) && !region.Contains((Y - 1, X + 1)))
                result++;
            // x x
            // . x
            if (region.Contains((Y, X - 1)) && region.Contains((Y + 1, X)) && !region.Contains((Y + 1, X - 1)))
                result++;
            // . x
            // x x
            if (region.Contains((Y, X - 1)) && region.Contains((Y - 1, X)) && !region.Contains((Y - 1, X - 1)))
                result++;
            // x x
            // x . 
            if (region.Contains((Y, X + 1)) && region.Contains((Y + 1, X)) && !region.Contains((Y + 1, X + 1)))
                result++;
        }

        return result;
    }

